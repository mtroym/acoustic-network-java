    private static final short CRC8_POLYNOMIAL = 0xB2;
    public static short[] createLookupTable()
    {
        short[] lookupTable = new short[256];
        for(int i=0; i<256; i++)
        {
            int rem = i; // remainder from polynomial division
            for(int j=0; j<8; j++)
            {
                if((rem & 1) == 1)
                {
                    rem >>= 1;
                    rem ^= CRC8_POLYNOMIAL;
                }
                else
                {
                    rem >>= 1;
                }
            }
            lookupTable[i] = (short)rem;
        }
        return( lookupTable );
    }

