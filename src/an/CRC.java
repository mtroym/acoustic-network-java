    private final boolean useLookupTable;
    private short crc8;


    public CRC()
    {
        this(true);
    }


    public CRC(boolean use_lookup_table)
    {
        useLookupTable = use_lookup_table;
        reset();
    }


    public CRC reset()
    {
        crc8 = CRC8_INIT_VALUE;
        return( this );
    }


    public CRC update(byte b)
    {
        if( useLookupTable )
        {
            crc8 = CRC8_LOOKUP_TABLE[(crc8 ^ b) & 0xFF];
        }
        else
        {
            crc8 ^= b;
            crc8 &= 0xFF;
            for(int j=0; j<8; j++)
            {
                if((crc8 & 1) == 1)
                {
                    crc8 >>= 1;
                    crc8 ^= CRC8_POLYNOMIAL;
                }
                else
                {
                    crc8 >>= 1;
                }
            }
        }
        return( this );
    }


    public CRC update(byte[] data, int offset, int length)
    {
        for(int i=offset; i < length; i++)
        {
            update( data[i] );
        }
        return( this );
    }


    public CRC update(byte[] data)
    {
        return update(data, 0, data.length);
    }


    public CRC update(String s)
    {
        try
        {
            return update(s.getBytes("UTF-8"));
        }
        catch(UnsupportedEncodingException ex)
        {
            throw new RuntimeException(ex);
        }
    }


    public short get()
    {
        return( (short)(crc8 ^ 0xFF) );
    }


    /**
     * Return calculated CRC8 in 2 capital hex digits with leading zeros.
     */
    public String getHex()
    {
        return( String.format("%02X", get()) );
    }

